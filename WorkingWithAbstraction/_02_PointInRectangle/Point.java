package WorkingWithAbstraction._02_PointInRectangle;

public class Point {

    int x;
    int y;

    //coordinates X and Y

    public Point(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
